    <h2>Tipos de Licencia</h2>
    <p>
      El software no se vende, se licencia. Una licencia es aquella autorización
      formal con carácter contractual que un autor de un software da a un
      interesado para ejercer "actos de explotación legales". Es decir, el
      software no se compra, sino que se adquieren una serie de derechos sobre
      el uso que se le puede dar. En las licencias de software libre esos
      derechos son muy abiertos y permisivos, apenas hay restricciones al uso de
      los programas. De ahí que ayude al desarrollo de la cultura. Pueden
      existir tantas licencias como acuerdos concretos se den entre el autor y
      el licenciatario. Desde el punto de vista del software libre, existen
      distintas variantes del concepto o grupos de licencias. A continuación
      detallamos algunas de ellas.
    </p>
    <p>
      <u>Licencias GPL:</u> Una de las más utilizadas es la Licencia Pública
      General de GNU (GNU GPL). El autor conserva los derechos de autor
      (copyright), y permite la redistribución y modificación bajo términos
      diseñados para asegurarse de que todas las versiones modificadas del
      software permanecen bajo los términos más restrictivos de la propia GNU
      GPL. Esto hace que sea imposible crear un producto con partes no
      licenciadas GPL: el conjunto tiene que ser GPL. Es decir, la licencia GNU
      GPL posibilita la modificación y redistribución del software, pero
      únicamente bajo esa misma licencia. Y añade que si se reutiliza en un
      mismo programa código "A" licenciado bajo licencia GNU GPL y código "B"
      licenciado bajo otro tipo de licencia libre, el código final "C",
      independientemente de la cantidad y calidad de cada uno de los códigos "A"
      y "B", debe estar bajo la licencia GNU GPL.
    </p>
    <p>
      <u>Licencias LGPL:</u> La Licencia Pública General Reducida de GNU, o más
      conocida por su nombre en inglés GNU Lesser General Public License, es una
      licencia creada por la (FSF) que garantiza la libertad de compartir y
      modificar el software cubierto por ella, asegurando que el software es
      libre para todos sus usuarios. Esta licencia se aplica a cualquier
      programa o trabajo que contenga una nota puesta por el propietario de los
      derechos del trabajo estableciendo que su trabajo puede ser distribuido
      bajo los términos de esta. El "Programa", utilizado en lo subsecuente, se
      refiere a cualquier programa o trabajo original, y el "trabajo basado en
      el Programa" significa que el programa o cualquier trabajo derivado del
      mismo bajo la ley de derechos de autor.
    </p>
    <p>
      <u>Copyleft: Hay</u> que hacer constar que el titular de los derechos de
      autor (copyright) de un software bajo licencia copyleft puede también
      realizar una versión modificada bajo su copyright original, y venderla
      bajo cualquier licencia que desee, además de distribuir la versión
      original como software libre. Esta técnica ha sido usada como un modelo de
      negocio por una serie de empresas que realizan software libre (por ejemplo
      MySQL); esta práctica no restringe ninguno de los derechos otorgados a los
      usuarios de la versión copyleft. <u>Licencias AGPL:</u> La Licencia
      Pública General de Affero (en inglés Affero General Public License,
      también Affero GPL o AGPL) es una licencia copyleft derivada de la
      Licencia Pública General de GNU diseñada específicamente para asegurar la
      cooperación con la comunidad en el caso de software que corra en
      servidores de red. La Affero GPL es íntegramente una GNU GPL con una
      cláusula nueva que añade la obligación de distribuir el software si éste
      se ejecuta para ofrecer servicios a través de una red de ordenadores. La
      Free Software Foundation recomienda que el uso de la GNU AGPLv3 sea
      considerado para cualquier software que usualmente corra sobre una red.
    </p>
    <p>
      <u>Licencias Estilo BSD:</u> Llamadas así porque se utilizan en gran
      cantidad de software distribuido junto a los sistemas operativos BSD. El
      autor, bajo tales licencias, mantiene la protección de copyright
      únicamente para la renuncia de garantía y para requerir la adecuada
      atribución de la autoría en trabajos derivados, pero permite la libre
      redistribución y modificación, incluso si dichos trabajos tienen
      propietario. Son muy permisivas, tanto que son fácilmente absorbidas al
      ser mezcladas con la licencia GNU GPL con quienes son compatibles. Puede
      argumentarse que esta licencia asegura “verdadero” software libre, en el
      sentido que el usuario tiene libertad ilimitada con respecto al software,
      y que puede decidir incluso redistribuirlo como no libre.
    </p>
