    <h2>¿Qué es el software libre?</h2>
    <p>
      «Software libre» es el software que respeta la libertad de los usuarios y
      la comunidad. A grandes rasgos, significa que los usuarios tienen la
      libertad de ejecutar, copiar, distribuir, estudiar, modificar y mejorar el
      software. Es decir, el «software libre» es una cuestión de libertad, no de
      precio. Para entender el concepto, piense en «libre» como en «libre
      expresión», no como en «barra libre». En inglés a veces decimos «libre
      software», en lugar de «free software», para mostrar que no queremos decir
      que es gratuito. Promovemos estas libertades porque todos merecen
      tenerlas. Con estas libertades, los usuarios (tanto individualmente como
      en forma colectiva) controlan el programa y lo que este hace. Cuando los
      usuarios no controlan el programa, decimos que dicho programa «no es
      libre», o que es «privativo». Un programa que no es libre controla a los
      usuarios, y el programador controla el programa, con lo cual el programa
      resulta ser un instrumento de poder injusto.
    </p>
    <h2>Las cuatro libertades esenciales</h2>
    <ul>
      <li>
        La libertad de ejecutar el programa como se desea, con cualquier
        propósito <u>(Libertad 0)</u>.
      </li>
      <li>
        La libertad de estudiar cómo funciona el programa, y cambiarlo para que
        haga lo que usted quiera <u>(libertad 1)</u>.
      </li>
      <li>
        El acceso al código fuente es una condición necesaria para ello. La
        libertad de redistribuir copias para ayudar a su prójimo
        <u>(libertad 2)</u>.
      </li>
      <li>
        La libertad de distribuir copias de sus versiones modificadas a terceros
        <u>(libertad 3)</u>.
      </li>
    </ul>
    <p>
      Esto le permite ofrecer a toda la comunidad la oportunidad de beneficiarse
      de las modificaciones. El acceso al código fuente es una condición
      necesaria para ello.
    </p>

    <h2>Linux y el sistema GNU</h2>
    <p>
      Muchos usuarios de ordenadores ejecutan a diario, sin saberlo, una versión
      modificada del sistema GNU. Debido a un peculiar giro de los
      acontecimientos, a la versión de GNU ampliamente utilizada hoy en día se
      la llama a menudo «Linux», y muchos de quienes la usan no se dan cuenta de
      que básicamente se trata del sistema GNU, desarrollado por el proyecto
      GNU. Efectivamente existe un Linux, y estas personas lo usan, pero
      constituye solo una parte del sistema que utilizan. Linux es el núcleo: el
      programa del sistema que se encarga de asignar los recursos de la máquina
      a los demás programas que el usuario ejecuta. El núcleo es una parte
      esencial de un sistema operativo, pero inútil por sí mismo, sólo puede
      funcionar en el marco de un sistema operativo completo. Linux se utiliza
      normalmente en combinación con el sistema operativo GNU: el sistema
      completo es básicamente GNU al que se le ha añadido Linux, es decir,
      GNU/Linux. Todas las distribuciones denominadas «Linux» son en realidad
      distribuciones GNU/Linux.
    </p>
